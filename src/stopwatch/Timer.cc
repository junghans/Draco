#include "Timer.hh"

namespace rtt_stopwatch
{
 
void Timer::start()
{
    time -= mysec();
    timerState = ON;
    ++count;
}

void Timer::stop()
{
    time += mysec();
    timerState = OFF;
}

} // end namespace rtt_stopwatch

