#include "3T/test/PreCond.hh"

//---------------------------------------------------------------------------//
// Destructor.
//---------------------------------------------------------------------------//

template<class T>
PreCond<T>::~PreCond() {}

//---------------------------------------------------------------------------//
// Constructor.
//---------------------------------------------------------------------------//

template<class T>
PreCond<T>::PreCond()
    : PCG_PreCond<T>() {}

//---------------------------------------------------------------------------//
// Solve QL * x = b for x.
//---------------------------------------------------------------------------//

template<class T>
void PreCond<T>::Left_PreCond( Mat1<T>& x, Mat1<T>&b )
{
    x = b;
}

//---------------------------------------------------------------------------//
// Solve QR * x = b for x.
//---------------------------------------------------------------------------//

template<class T>
void PreCond<T>::Right_PreCond( Mat1<T>& x, Mat1<T>&b )
{
    x = b;
}

