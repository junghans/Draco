#include "t3/T3_PreCond.hh"

//---------------------------------------------------------------------------//
// Destructor.
//---------------------------------------------------------------------------//

template<class T>
T3_PreCond<T>::~T3_PreCond() {}

//---------------------------------------------------------------------------//
// Constructor.
//---------------------------------------------------------------------------//

template<class T>
T3_PreCond<T>::T3_PreCond()
    : PCG_PreCond<T>() {}

//---------------------------------------------------------------------------//
// Solve QL * x = b for x.
//---------------------------------------------------------------------------//

template<class T>
void T3_PreCond<T>::Left_PreCond( Mat1<T>& x, Mat1<T>&b )
{
    x = b;
}

//---------------------------------------------------------------------------//
// Solve QR * x = b for x.
//---------------------------------------------------------------------------//

template<class T>
void T3_PreCond<T>::Right_PreCond( Mat1<T>& x, Mat1<T>&b )
{
    x = b;
}

