#include "util/dump.hh"

void dump( ADFile *f, char *buf, dsxx::String id )
{
    ADKey adk;
    sprintf( adk.s, "Text: %s", &id[0] );
    f->Start_new_entry(adk);
    f->write( buf, strlen(buf) );
    f->End_of_entry();
}

