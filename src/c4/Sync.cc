#include "Sync.hh"
#include "global.hh"  

namespace rtt_c4
{

//---------------------------------------------------------------------------//
// HSync constructor.  Forces a global sync before continuing.
//---------------------------------------------------------------------------//

HSync::HSync( int s /*=1*/ )
{
    if (s)
	global_barrier();
}

//---------------------------------------------------------------------------//
// TSync destructor.  Forces a global sync before continuing. 
//---------------------------------------------------------------------------//

TSync::~TSync()
{
    if (sync)
	global_barrier();
}

} // end of rtt_c4

