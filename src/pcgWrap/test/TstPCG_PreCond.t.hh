#include "TstPCG_PreCond.hh"

//---------------------------------------------------------------------------//
// Destructor.
//---------------------------------------------------------------------------//

template<class T>
TstPCG_PreCond<T>::
~TstPCG_PreCond()
{
}

//---------------------------------------------------------------------------//
// Constructor.
//---------------------------------------------------------------------------//

template<class T>
TstPCG_PreCond<T>::
TstPCG_PreCond()
{
}

//---------------------------------------------------------------------------//
// Solve QL * x = b for x.
//---------------------------------------------------------------------------//

template<class T>
void TstPCG_PreCond<T>::
Left_PreCond( rtt_dsxx::Mat1<T>& x,
	      const rtt_dsxx::Mat1<T>&b )
{
    x = b;
}

//---------------------------------------------------------------------------//
// Solve QR * x = b for x.
//---------------------------------------------------------------------------//

template<class T>
void TstPCG_PreCond<T>::
Right_PreCond( rtt_dsxx::Mat1<T>& x,
	       const rtt_dsxx::Mat1<T>&b )
{
    x = b;
}

